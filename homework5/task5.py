import random
# Выводит все элементы списка, которые больше предыдущего.
def pr(ns):
  if len(ns) < 2:
    print("Список должен содержать хотя бы два элемента.")
    return

  # Добавление последнего элемента в начало списка для сравнения с первым
  ns = ns[-1:] + ns

  for i in range(1, len(ns)):
    if ns[i] > ns[i - 1]:
      print(ns[i])

# Генерация случайного списка чисел
ns = []
for _ in range(10):
    ns.append(random.randint(1, 100))

# Вывод списка
print("Исходный список:", ns)

# Вывод элементов, больших предыдущего
print("Элементы, большие предыдущего:")
pr(ns)

